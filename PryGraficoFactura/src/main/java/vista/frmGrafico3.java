    negocio obj = new negocio();
    DecimalFormat sd=new DecimalFormat("0.00");
    void torta(){
        DefaultPieDataset ds = new DefaultPieDataset();
        double sm=0;
        
        /*for(reporte1 x:obj.lisMes(an)){
            ds.setValue(x.Lmes()+"-"+x.getTotal(), x.getTotal());
        }*/
        
        for(reporte1 x:obj.lisProducto2()){
            sm+=x.getTotal();
        }
        
        double porc;
        
        for(reporte1 x:obj.lisProducto2()){
            porc=(x.getTotal()/sm)*100;
            ds.setValue(x.getAño()+"-"+sd.format(porc)+"%", x.getTotal());
        }
    
        //Graficar en memoria
        JFreeChart gf=ChartFactory.createPieChart3D("Año",ds);
        ChartPanel cp = new ChartPanel(gf);
        panel1.removeAll();
        panel1.setLayout(new java.awt.BorderLayout());
        panel1.add(cp,BorderLayout.CENTER);
        panel1.validate();
        
    }
    
    void barra(){
        DefaultCategoryDataset ds=new DefaultCategoryDataset();
        for(reporte1 x:obj.lisProducto2()){
            ds.setValue(x.getTotal(), "Venta", "Año "+x.getAño());
        }JFreeChart gf=ChartFactory.createBarChart3D("Venta fin de año", "Monto", "Año", ds) ;
        ChartPanel cp=new ChartPanel(gf);
        panel1.removeAll();
        panel1.setLayout(new java.awt.BorderLayout());
        panel1.add(cp,BorderLayout.CENTER);
        panel1.validate();
    }
    
    void lineal(){
        DefaultCategoryDataset ds=new DefaultCategoryDataset();
        for(reporte1 x:obj.lisProducto2()){
            ds.setValue(x.getTotal(), "Venta", "Año "+x.getAño());
        }JFreeChart gf=ChartFactory.createLineChart("Venta fin de año", "Monto", "Año", ds) ;
        ChartPanel cp=new ChartPanel(gf);
        panel1.removeAll();
        panel1.setLayout(new java.awt.BorderLayout());
        panel1.add(cp,BorderLayout.CENTER);
        panel1.validate();
    }
